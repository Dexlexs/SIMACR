    private void itmSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_itmSalvarMouseClicked
        if (cbxTipo.getSelectedIndex() < 1) {
            JOptionPane.showMessageDialog(this, "Selecionar um tipo de utensÃ­lio");
        } else {
            Integer quantidade;
            try {
                quantidade = Integer.parseInt(txtQuantidade.getText());
                if(quantidade<0)
                {
                    JOptionPane.showMessageDialog(this, "Quantidade somente numeros positivos");
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Quantidade somente numeros inteiros");
                return;
            }
            if (txtNome.getText().trim().equals("")) {
                controller.salvar(null, quantidade, String.valueOf(cbxTipo.getSelectedItem()));
            }
             else {
                controller.salvar(txtNome.getText().trim(), quantidade, String.valueOf(cbxTipo.getSelectedItem()));
            }
        }
    }//GEN-LAST:event_itmSalvarMouseClicked

