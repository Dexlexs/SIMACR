    public void startcbox() {
        limparcbox();
        for (IEntidadeItemEstoque it : controller.carregaUtensilios()) {
            cbxTipoElemento.addItem(it.getNome());
        }
    }
    
    public void limparcbox() {
        cbxTipoElemento.removeAllItems();
        cbxTipoElemento.addItem("");
    }    
    
