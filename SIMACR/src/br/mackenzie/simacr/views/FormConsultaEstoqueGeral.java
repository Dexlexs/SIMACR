    private void tblPesquisaEstoqueMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPesquisaEstoqueMouseClicked
        if (evt.getClickCount() > 1) {
            JTable target = (JTable) evt.getSource();
            controller.abrirTipoUtensilio((Long) modelo.getValueAt(target.getSelectedRow(), 2));
        }
    }//GEN-LAST:event_tblPesquisaEstoqueMouseClicked
    
    private void btnVerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerActionPerformed
        if (cbxTipoElemento.getSelectedIndex() > 0) {
            controller.abrirTipoUtensilio(controller.getIdUtensilio(String.valueOf(cbxTipoElemento.getSelectedItem())));
        }
    }//GEN-LAST:event_btnVerActionPerformed
    
    private void cbxTipoElementoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbxTipoElementoItemStateChanged
        if (cbxTipoElemento.getSelectedIndex() < 1) {
            btnVer.setEnabled(false);
        } else {
            btnVer.setEnabled(true);
        }
    }//GEN-LAST:event_cbxTipoElementoItemStateChanged
    
    public void limparTabela() {
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
    }
    
    public void fazerPesquisa() {
        limparTabela();
        for (IEntidadeItemEstoque it : controller.pesquisar(txtPesquisa.getText())) {
            modelo.insertRow(modelo.getRowCount(), new Object[]{it.getNome(), it.getTotal(), it.getId()});
        }
    }
