    public FormProntuario(IControllerFormProntuario varController, IPaciente varPaciente, boolean tipo) {
        initComponents();
        controller = varController;
        modelo = (DefaultTableModel) tblConsulta.getModel();
        populaDadosPaciente(varPaciente);
        if (tipo) {
            initTableMedico();
            populaConsultaMedica();
        }
        else
        {
            initTableDentista();
        }
        this.setVisible(true);
    }

    private void populaConsultaMedica() {
        ArrayList<IConsultaMedica> var = controller.buscarConsultasMedica();
        for (IConsultaMedica cm : var) {
            modelo.insertRow(modelo.getRowCount(), new Object[]{cm.getId(), cm.getDataConsulta(), cm.getSintoma(),cm.getHipoteseDiagnostica(),cm.getResponsavel().getNome()});
        }
    }
    
    private void populaConsultaOdontologica() {
        ArrayList<IConsultaOdontologica> var = controller.buscarConsultasOdontologicas();
        for (IConsultaOdontologica cm : var) {
            modelo.insertRow(modelo.getRowCount(), new Object[]{cm.getId(), cm.getDataConsulta(), cm.getObservacao(),cm.getEvolucao(),cm.getResponsavel().getNome()});
        }
    }    

    private void populaDadosPaciente(IPaciente var) {
        txtNome.setText(var.getNome());
        txtData_Nascimento.setText(var.getDataNascimento());
        txtCor.setText(var.getCor());
        txtComunidade.setText(var.getComunidade());
        txtRio.setText(var.getRio());
        txtProfissao.setText(var.getProfissao());
        txtTelefone.setText(var.getTelefone());
        txtNaturalidade.setText(var.getNaturalidade());
        txtEstado_Civil.setText(var.getEstadoCivil());
        if (var.getSexo().equals("Masculino")) {
            rbtnMasculino.setSelected(true);
        } else {
            rbtnFeminino.setSelected(true);
        }

    }

