    private void initTableMedico() {
        modelo = (DefaultTableModel) tblConsulta.getModel();
        modelo.setDataVector(new Object[][]{}, new Object[]{"IDConsulta", "Data", "Hipótese", "Diagnóstico", "Responśavel"});
        tblConsulta.removeColumn(tblConsulta.getColumnModel().getColumn(0));
    }
    
    private void initTableDentista()
    {
        modelo = (DefaultTableModel) tblConsulta.getModel();
        modelo.setDataVector(new Object[][]{}, new Object[]{"IDConsulta", "Data", "Observação","Evolução", "Responśavel"});
        tblConsulta.removeColumn(tblConsulta.getColumnModel().getColumn(0));
    }

    private void mnuVerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mnuVerMouseClicked
        if (tblConsulta.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Selecionar uma consulta");
        } else {

            controller.abrirConsultaMedica((Long) tblConsulta.getValueAt(tblConsulta.getSelectedRow(), 0));
        }
    }//GEN-LAST:event_mnuVerMouseClicked

