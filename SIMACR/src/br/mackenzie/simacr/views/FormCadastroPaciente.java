    public void startCbox(int tipo) {
        switch (tipo) {
            case (0):
                cboxComunidade.setEnabled(false);
                limparcboxcomunidade("");
                carregarRios("");
                carregarCorPele("");
                break;
            case (1):
                carregarCorPele("");
                break;
            case (2):
                cboxComunidade.setEnabled(false);
                limparcboxcomunidade("");
                carregarRios("");
                break;
            case (3):
                limparcboxcomunidade("");
                carregarComunidade("");
                break;
            case (4):
                cboxComunidade.setEnabled(true);
                btnEditComunidade.setVisible(false);
                btnEditRio.setVisible(false);
                btnEditarCor.setVisible(false);
        }

    }

