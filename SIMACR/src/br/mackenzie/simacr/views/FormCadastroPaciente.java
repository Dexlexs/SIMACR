    private void itmSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_itmSalvarMouseClicked
        if ((txtNome.getText().trim()).equals("")) {
            JOptionPane.showMessageDialog(this, "Informar o nome");
        } else if (String.valueOf(cboxRio.getSelectedItem()).equals("")) {
            JOptionPane.showMessageDialog(this, "Informar o rio");
        } else if (String.valueOf(cboxComunidade.getSelectedItem()).equals("")) {
            JOptionPane.showMessageDialog(this, "Informar a comunidade");
        } else if (txtDataNascimento.isValid() == false) {
            JOptionPane.showMessageDialog(this, "Preencher a data de nascimento");
        } else {
            controller.salvar(txtNome.getText(), txtProfissao.getText(), String.valueOf(cboxComunidade.getSelectedItem()), String.valueOf(cboxRio.getSelectedItem()), txtDataNascimento.getText(), txtTelefone.getText(), String.valueOf(cboxCor.getSelectedItem()), txtNaturalidade.getText(), txtNacionalidade.getText(), String.valueOf(cboxEstadoCivil.getSelectedItem()), String.valueOf(cboxSexo.getSelectedItem()));
        }
    }//GEN-LAST:event_itmSalvarMouseClicked

