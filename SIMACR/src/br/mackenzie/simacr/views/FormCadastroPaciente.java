    private void carregarRios(String primeiro) {
        limparcboxrio(primeiro);
        for (IEntidadeComUmaString it : controller.carregaRio()) {
            cboxRio.addItem(it.getNome());
        }
    }

    private void carregarCorPele(String primeiro) {
        limparcboxcorpele(primeiro);
        for (IEntidadeComUmaString it : controller.carregaCorPele()) {
            cboxCor.addItem(it.getNome());
        }
    }

    private void carregarComunidade(String primeiro) {
        limparcboxcomunidade(primeiro);
        for (IEntidadeComUmaString it : controller.carregaComunidade(String.valueOf(cboxRio.getSelectedItem()))) {
            cboxComunidade.addItem(it.getNome());
        }
    }

    private void limparcboxcomunidade(String primeiro) {
        cboxComunidade.removeAllItems();
        cboxComunidade.addItem(primeiro);
    }

    private void limparcboxrio(String primeiro) {
        cboxRio.removeAllItems();
        cboxRio.addItem(primeiro);
    }

    private void limparcboxcorpele(String primeiro) {
        cboxCor.removeAllItems();
        cboxCor.addItem(primeiro);
    }
